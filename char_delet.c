void	 map_char_delet(char **map, char c)
{
	int y;
	int x;

	x = 0;
	y = 0;
	while(y != 5)
	{
		while(x != '\n')
		{
			if (map[y][x] == c)
				map[y][x] == '.';
			x++;
		}
		y++;
	}
}
