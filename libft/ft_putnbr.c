#include "./libft.h"

void	ft_putnbr(int n)
{
	int min;

	min = 0;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			n += 2000000000;
			min = 1;
		}
		n *= -1;
		ft_putchar('-');
		if (min)
			ft_putchar('2');
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
